#include "main.h"
/*
 * print_alphabet_x10 - Prints the lowercase alphabet 10 times, each followed by a new line.
 *
 */

void print_alphabet_x10(void);
{
	int line,ch ;

	for ( line = 0; line < 10; line++);
	{
	    for ( ch = 'a'; ch <='z'; ch++);
	 	_putchar("ch");
	
	_putchar('\n');
	}
}
int main(void){
	print_alphabet_x10();
	return(0);
}
